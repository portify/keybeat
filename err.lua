				local globalkey = getkeyname(_G, info.func)
				if globalkey ~= nil then
					return fmt_in_func(globalkey)
				end
			end

			return fmt_in_func(info.name)
		elseif info.what == "C" then
			local name = getkeyname(_G, info.func)
			if name ~= nil then
				return fmt_in_func(name)
