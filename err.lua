local function getkeyname(table, value, cycle)
	if cycle then
		if cycle[table] then
			return
		end
		cycle[table] = true
	else
		cycle = {table}
	end

	local subkey, subdepth

	for k, v in pairs(table) do
		if type(k) == "string" then
			if v == value then
				return k, 0
			elseif type(v) == "table" then
				local key, depth = getkeyname(v, value, cycle)
				if key ~= nil and (subkey == nil or depth < subdepth) then
					subkey = k .. "." .. key
					subdepth = depth
				end
			end
		end
	end

	if subkey then
		return subkey, subdepth + 1
	end
end

local function inspect(value, depth, seen)
	-- if known_values[value] then
	-- 	return known_values[value]
	-- end

	local globalkey = getkeyname(_G, value)

	if globalkey ~= nil then
		return globalkey
	end

	if type(value) == "table" then
		if depth then
			if depth > 3 then
				return "..."
			end

			depth = depth + 1
		else
			depth = 2
		end

		if seen then
			if seen[value] then
				return "<cycle>"
			end

			seen[value] = true
		else
			seen = {value}
		end

		local parts = {}
		local keys_left = 5

		for k, v in pairs(value) do
			keys_left = keys_left - 1
			if keys_left <= 0 then break end

			if type(k) ~= "string" then
				k = "[" .. inspect(k, depth, seen) .. "]"
			end

			table.insert(parts, k .. " = " .. inspect(v))
		end

		return "{" .. table.concat(parts, ", ") .. "}"
	elseif type(value) == "string" then
		return solarized.yellow, (string.format("%q", value):gsub("\\\n", "\\n"))
	elseif type(value) == "number" or type(value) == "boolean"
			or type(value) == "nil" then
		return solarized.violet, tostring(value)
	else
		return tostring(value)
	end
end

local function getinfocode(info)
	if info.currentline < 1 then
		return
	end

	local file = io.open(info.short_src)
	if file == nil then return end

	local parts = {}

	local nprev = info.currentline - 1
	local ncurr = info.currentline
	local nnext = info.currentline + 1

	local lineno = 1
	for line in file:lines() do
		if lineno == nprev or lineno == ncurr or lineno == nnext then
			if line:gsub("^%s+", "") ~= "" then
				if lineno == ncurr then
					insertall(parts, "\n     ", solarized.violet, "-> ", solarized.base3, line)
				else
					insertall(parts, "\n        ", solarized.base1, line)
				end
			end
		elseif lineno > ncurr then
			break
		end

		lineno = lineno + 1
	end

	file:close()

	if #parts == 0 then
		return
	end

	return parts
end

